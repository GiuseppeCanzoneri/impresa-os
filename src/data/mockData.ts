export function formatEuro(value: number): string {
  return new Intl.NumberFormat('it-IT', {
    style: 'currency',
    currency: 'EUR',
  }).format(value);
}

export function getProjectMargin(project: Project): number {
  if (project.revenues_to_date <= 0) return project.margin_estimated;
  return Math.round(((project.revenues_to_date - project.costs_to_date) / project.revenues_to_date) * 100);
}
